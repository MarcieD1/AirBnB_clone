def calculate_sum(a, b):
    """Calculate the sum of two numbers."""
    result = a + b
    return result


def main():
    """Main function to demonstrate code style."""
    num1 = 5
    num2 = 10
    sum_result = calculate_sum(num1, num2)
    print(f"The sum of {num1} and {num2} is: {sum_result}")


if __name__ == "__main__":
    main()
